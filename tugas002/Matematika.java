package tugas002;



public class Matematika extends MathRun{
    
     

    // Metode untuk pertambahan dua angka
    public static double pertambahan(double a, double b) {
        return a + b;
    }

    // Metode untuk pertambahan tiga angka (overloading)
    public static double pertambahan(double a, double b, double c) {
        return a + b + c;
    }

    // Metode untuk pengurangan
    public static double pengurangan(double a, double b) {
        return a - b;
    }

    // Metode untuk perkalian
    public static double perkalian(double a, double b) {
        return a * b;
    }

    // Metode untuk pembagian
    public static double pembagian(double a, double b) {
        return a / b;
    }

    // Metode untuk modulus
    public static double modulus(double a, double b) {
        return a % b;
    }
}
    




